import type { PlayerDef, Position, SquadDef } from "@/lib/game/types";

type Row = [name: string, positions: string, ovr: number];

function slugify(name: string): string {
  return name
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "");
}

